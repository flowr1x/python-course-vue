            <h2 class="text-manual__section-title">Ввод данных. Функция input()</h2>
            <div class="text-manual__section-block">
              <p>За ввод в программу данных с клавиатуры в Python отвечает функция <b>input()</b>. Когда
                  вызывается эта функция, программа останавливает свое выполнение и ждет, когда
                  пользователь введет текст. После этого, когда он нажмет Enter, функция input() заберет
                  введенный текст и передаст его программе, которая уже будет обрабатывать его согласно
                  своим алгоритмам.</p>
              <p>Если в интерактивном режиме ввести команду input(), то ничего интересного вы не увидите.
                  Компьютер будет ждать, когда вы что-нибудь введете и нажмете Enter или просто нажмете
                  Enter. Если вы что4то ввели, это сразу же отобразиться на экране:</p>
              <pre><code class="python">>>> input()
    Yes!
    'Yes!'</code></pre>
              <p>Функция input() передает введенные данные в программу. Их можно присвоить переменной.
                  В этом случае интерпретатор не выводит строку сразу же:</p>
              <pre><code class="python">>>> answer = input()
    No, it is not.</code></pre>
              <p>В данном случае строка сохраняется в переменной answer, и при желании мы можем
                  вывести ее значение на экран:</p>
              <pre><code class="python">>>> answer
    'No, it is not.'</code></pre>
              <p>При использовании функции print() кавычки в выводе опускаются:</p>
              <pre><code class="python">>>> print(answer)
    No, it is not.</code></pre>
              <p>Куда интересней использовать функцию input() в скриптах – файлах с кодом. Рассмотримакую программу:</p>
