            <div class="text-manual__section-block">
              <p>Приведенную выше операцию все4таки можно выполнить, если превратить число 1 в строку
                  "1". Для изменения одних типов данных в другие в языке Python предусмотрен ряд
                  встроенных в него функций (что такое функция в принципе, вы узнаете в других уроках).
                  Поскольку мы пока работаем только с тремя типами (int, float и str), то рассмотрим только
                  соответствующие им функции: int(), float(), str().</p>
              <pre><code class="python">>>> str(1) + 'a'
'1a'
>>> float('3.2') + int('2')
5.2
>>> str(4) + str(1.2)
'41.2'</code></pre>
              <p>Эти функции преобразуют то, что помещается в их скобки соответственно в целое число,
                  вещественное число или строку. Однако надо понимать, что преобразовать можно не все:</p>
              <pre><code class="python">>>> int('hi')
Traceback (most recent call last):
File "&lt;stdin&gt;", line 1, in &lt;module&gt;
ValueError: invalid literal for int() with base 10: 'hi'</code></pre>
              <p>Здесь возникла ошибка значения (ValueError), так как передан литерал (в данном случае
                  строка с буквенными символами), который нельзя преобразовать к числу с основанием 10.
                  Однако функция int() не такая простая:</p>
              <pre><code class="python">>>> int('101', 2)
5
>>> int('F', 16)
15</code></pre>
                <p>Если вы знаете о различных системах счисления, то поймете, что здесь произошло.</p>
                <p>Обратите внимание еще на одно. Данные могут называться значениями, а также
                    литералами. Эти три понятия ("данные", "значение", "литерал") не обозначают одно и то же,
                    но близки и нередко употребляются как синонимы. Чтобы понять различие между ними,
                    места их употребления, надо изучить программирование глубже.</p>
