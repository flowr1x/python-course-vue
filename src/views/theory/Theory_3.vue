          <section  class="text-manual__doc-section">
            <h2 class="text-manual__section-title">Переменные</h2>
            <div class="text-manual__section-block">
              <p>Данные хранятся в ячейках памяти компьютера. Когда мы вводим число, оно помещается в
                какую4то ячейку памяти. Но как потом узнать, куда именно? Как впоследствии обращаться к
                этим данными? Нужно как4то запомнить, пометить соответствующую ячейку.</p>
              <p>Раньше, при написании программ на машинном языке, обращение к ячейкам памяти
                осуществляли с помощью указания их регистров, т. е. конкретно сообщали, куда положить
                данные и откуда их взять. Однако с появлением ассемблеров при обращении к данным стали
                использовать словесные <b>переменные</b>, что куда удобней для человека.</p>
              <p>Механизм связи между переменными и данными может различаться в зависимости от языка
                программирования и типов данных. Пока достаточно запомнить, что в программе данные
                связываются с каким4либо именем и в дальнейшем обращение к ним возможно по этому
                имени переменной.</p>
              <p>Слово "переменная" обозначает, что сущность может меняться, она непостоянна.
                Действительно, вы увидите это в дальнейшем, одна и та же переменная может быть связана
                сначала с одними данными, а потом – с другими. То есть ее значение может меняться, она переменчива.</p>
              <p>В программе на языке Python, как и на большинстве других языков, связь между данными и
                переменными устанавливается с помощью знака =. Такая операция называется
                <b>присваивание</b> (также говорят "присвоение"). Например, выражение sq = 4 означает, что на
                объект, представляющий собой число 4, находящееся в определенной области памяти, теперь
                ссылается переменная sq, и обращаться к этому объекту следует по имени sq.</p>
              <p>Имена переменных могут быть любыми. Однако есть несколько общих правил их написания:</p>
              <div class="text-manual__textcols">
                <div class="textcols-item">
                  <ol>
                    <li>Желательно давать переменным осмысленные имена, говорящие о назначении
                        данных, на которые они ссылаются.
                    </li>
                    <li>Имя переменной не должно совпадать с командами языка (зарезервированными
                        ключевыми словами).
                    </li>
                    <li>Имя переменной должно начинаться с буквы или символа подчеркивания (_), но не с
                        цифры.
                    </li>
                    <li>Имя переменной не должно содержать пробелы.</li>
                    <li>Чтобы узнать значение, на которое ссылается переменная, находясь в режиме
                        интерпретатора, достаточно ее вызвать, т. е. написать имя и нажать Enter.
                    </li>
                  </ol>
                </div>
              </div>
              <pre><code class="python">>>> sq = 4
>>> sq 
4</code></pre>
              <p>Вот более сложный пример работы с переменными в интерактивном режиме:</p>
              <pre><code class="python">>>> apples = 100
>>> eat_day = 5
>>> day = 7
>>> apples = apples - eat_day * day
>>> apples
65  </code></pre>
              <p>Здесь фигурируют три переменные: apples, eat_day и day. Каждой из них присваивается свое
                значение. Выражение apples = apples - eat_day * day сложное. Сначала выполняется
                подвыражение, стоящее справа от знака равенства. После этого его результат присваивается
                переменной apples, в результате чего ее старое значение (100) теряется. В подвыражении
                apples - eat_day * day вместо имен переменных на самом деле используются их
                значения, т. е. числа 100, 5 и 7.</p>
