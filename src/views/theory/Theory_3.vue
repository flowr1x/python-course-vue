            <h2 class="text-manual__section-title">Операции в программировании</h2>
            <div class="text-manual__section-block">
              <p><b>Операция</b> – это выполнение каких-либо действий над данными, которые в данном случае
                  именуют <b>операндами</b>. Само действие выполняет <b>оператор</b> – специальный инструмент. Если
                  бы вы выполняли операцию постройки стола, то вашими операндами были бы доска и гвоздь, а оператором – молоток.</p>
              <p>Так в математике и программировании символ плюса является оператором операции
                  сложения по отношению к числам. В случае строк этот же оператор выполняет операцию
                  <em>конкатенации</em>, т. е. соединения</p>
              <pre><code class="python">>>> 10.25 + 98.36
108.61
>>> 'Hello' + 'World'
'HelloWorld'</code></pre>
              <p>Здесь следует для себя отметить, что то, что делает оператор в операции, зависит не только от
                  него, но и от типов данных, которыми он оперирует. Молоток в случае нападения на вас
                  крокодила перестанет играть роль строительного инструмента. Однако в большинстве
                  случаев операторы не универсальны. Например, знак плюса неприменим, если операндами
                  являются, с одной стороны, число, а с другой – строка.</p>
              <pre><code class="python">>>> 1 + 'a'
Traceback (most recent call last):
File "&lt;stdin&gt;", line 1, in &lt;module&gt;
TypeError: unsupported operand type(s) for +: 'int' and 'str'</code></pre>
              <p>Здесь в строке TypeError: unsupported operand type(s) for +: 'int' and 'str'
                    интерпретатор сообщает, что произошла ошибка типа – неподдерживаемый операнд для
                    типов int и str</p>
