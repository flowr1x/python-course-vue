        <h2 id="title1" class="text-manual__section-title">Определение списка</h2>
        <div class="text-manual__section-block">
          <p>Список в Python – это встроенный тип (класс) данных, представляющий собой одну из
            разновидностей структур данных. Структуру данных можно представить как сложную
            единицу, объединяющую в себе группу более простых. Каждая разновидность структур
            данных имеет свои особенности. Список – это изменяемая последовательность
            произвольных элементов.</p>
          <p>В подавляющем большинстве других языков программирования есть такой широко
            используемый тип данных как массив. В Питоне такого встроенного типа нет. Однако списки
            – это по своей сути массивы за одним исключением. Составляющие массив элементы
            должны принадлежать одному типу данных, для python-списков такого ограничения нет.</p>
          <p>Например, массив может содержать только целые числа или только вещественные числа или
            только строки. Список также может содержать элементы только одного типа, что делает его
            внешне неотличимым от массива. Но вполне допустимо, чтобы в одном списке содержались
            как числа, так и строки, а также что4нибудь еще.</p>
