      <section class="text-manual__doc-section">
        <h2 id="title3" class="text-manual__section-title">Математические функции</h2>
        <div class="text-manual__section-block">
          <p>Функция abs() возвращает абсолютное значение числа:</p>
          <pre><code class="python">>>> abs(-2.2)
2.2
>>> abs(9)
9</code></pre>
          <p>Если требуется округлить вещественное число до определенного знака после запятой, то
            следует воспользоваться функцией round():</p>
          <pre><code class="python">>>> a = 10/3
>>> a
3.3333333333333335
>>> round(a,2)
3.33
>>> round(a)
3</code></pre>
          <p>Если второй аргумент не задан, то округление идет до целого числа. Есть одна
            специфическая особенность этой функции. Второй аргумент может быть отрицательным
            числом. В этом случае округляться начинают единицы, десятки, сотни и т. д., то есть целая
            часть:</p>
          <pre><code class="python">>>> round(5321, -1)
5320
>>> round(5321, -3)
5000
>>> round(5321, -4)
10000</code></pre>
          <p>Функция именно округляет согласно правилу округления из математики, а не отбрасывает.</p>
          <p>Поэтому 5 тысяч неожиданно округляются до десяти. </p>
          <pre><code class="python">>>> round(3.76, 1)
3.8
>>> round(3.72, 1)
3.7
>>> round(3.72)
4
>>> round(3.22)
3</code></pre>
          <p>Если нужно просто избавиться от дробной части без округления, следует воспользоваться
              функцией int():</p>
          <pre><code class="python">>>> int(3.78)
3</code></pre>
          <p>Нередко функцию round() используют совместно с функцией print(), избегая форматирования
              вывода:</p>
          <pre><code class="python">>>> a = 3.45673
>>> print("Number: %.2f" % a)
Number: 3.46
>>> print("Number:", round(a,2))
Number: 3.46</code></pre>
          <p>В последнем случае код выглядит более ясным.</p>
          <p>Функция divmod() выполняет одновременно деление нацело и нахождение остатка от
              деления:</p>
          <pre><code class="python">>>> divmod(10, 3)
(3, 1)
>>> divmod(20, 7)
(2, 6)</code></pre>
          <p>Возвращает она кортеж, извлечение данных из которого мы пока не изучали. В других языках
              нередко встречаются две отдельные функции: div() и mod(). Первая делит нацело, вторая
              находит остаток от целочисленного деления (деления по модулю). В Python и многих других
              языках для этого используются специальные символы4операнды:</p>
          <pre><code class="python">>>> 10 // 3
3
>>> 10 % 3
1</code></pre>
          <p>Функция pow() возводит в степень. Первое число – основание, второе – показатель:</p>
          <pre><code class="python">>>> pow(3, 2)
9
>>> pow(2, 4)
16</code></pre>
          <p>То же самое можно проделать так:</p>
          <pre><code class="python">>>> 3**2
9
>>> 2**4
16</code></pre>
          <p>Однако pow() может принимать третий необязательный аргумент. Это число, на которое
            делится по модулю результат возведения в степень:</p>
          <pre><code class="python">>>> pow(2, 4, 4)
0
>>> 2**4 % 4
0</code></pre>
          <p>Преимуществом первого способа является его более быстрое выполнение.</p>
          <p>Функции max(), min() и sum() находят соответственно максимальный, минимальный элемент
            и сумму элементов аргумента:</p>
          <pre><code class="python">>>> max(10, 12, 3)
12
>>> min(10, 12, 3, 9)
3
>>> a = (10, 12, 3, 10)
>>> sum(a)
35</code></pre>
          <p>В sum() нельзя передать перечень элементов, должна быть структура данных, например,
              кортеж. В min() и max() также чаще передают один так называемый итерируемый объект:</p>
          <pre><code class="python">>>> max(a)
12</code></pre>
