        <h2 id="title2" class="text-manual__section-title">Функциия для работы с символами</h2>
        <div class="text-manual__section-block">
          <p>Функция ord() позволяет получить номер символа по таблице Unicode. Соответственно,
              принимает она в качестве аргумента одиночный символ, заключенный в кавычки:</p>
          <pre><code class="python">>>> ord('z')
122
>>> ord('ф')
1092
>>> ord('@')
64</code></pre>
          <p>Функция chr() выполняет обратное действие. Она позволяет получить символ по его номеру:</p>
          <pre><code class="python">>>> chr(87)
'W'
>>> chr(1049)
'Й'
>>> chr(10045)
'*'</code></pre>
          <p>Чтобы не путать ord() и chr(), помните, что функция – это действие. Ее имя как бы отвечает
              на вопрос "Что сделать?". Order – это порядок. Значит, мы хотим получить порядковый номер
              элемента в ряду. А чтобы получить номер, должны передать символ. Character – это символ.
              Значит, мы хотим получить символ. Поэтому должны передать порядковый номер.</p>
          <p>Функция len() в качестве аргумента принимает объект, который состоит из более простых
              объектов, количество которых она подсчитывает. Числа – это простые объекты, их нельзя
              передавать в len(). Строки можно:</p>
          <pre><code class="python">>>> len('abc')
3
>>> s1 = '------'
>>> s2 = '_______'
>>> len(s1) > len(s2)
False
>>> len(s1)
6
>>> len(s2)
7</code></pre>
          <p>Кроме строк в len() можно передавать другие, еще не изученные нами, структуры данных.</p>
        </div>
