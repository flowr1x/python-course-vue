        <h2 id="title1" class="text-manual__section-title">Определение random</h2>
        <div class="text-manual__section-block">
          <p>В компьютерных программах нередко требуется эмуляция случайности. Например, при
            разработке игр. Если в программе имеется некий генератор, т. е. производитель, случайного
            числа, то, используя полученное таким образом число, можно выбирать ту или иную ветку
            выполнения программы, или произвольный объект из коллекции. Другими словами, главное
            – сгенерировать число. Эмуляция случайности иного рода основывается на нем.</p>
          <p>Мы наверняка не знаем, есть ли в природе случайность, или она нам только кажется из4за
            ограниченности наших знаний. Мы только знаем, что в программировании настоящей
            случайности нет. Неоткуда взяться произвольному числу, нельзя запрограммировать его
            появление из ниоткуда. Можно лишь создать программу, которая в результате применения
            сложной формулы к "зерну" будет выдавать число, и нам будет казаться, что это число
            случайно.</p>
          <p>"Зерно" – это исходные данные для формулы. Им может быть, например, системное время в
            миллисекундах, которое постоянно меняется. Следовательно, "зерно" будет постоянно
            разным. Или программист может задавать его самостоятельно.</p>
          <p>Подобную программу (в реальности модуль или функцию) называют генератором
            псевдослучайных чисел. В состав стандартной библиотеки языка Python входит модуль
            random. Он содержит множество функций, связанных с эмуляцией случайности (например,
            "перемешивание" элементов последовательности), а не только функции генерации
            псевдослучайных чисел.</p>
          <p>В этом уроке будут рассмотрены функции random(), randrange() и randint() из модуля random.
            Обратите внимание, что модуль random содержит одноименную функцию random(). Так
            бывает.</p>
          <p>Чтобы обращаться к функциям, надо импортировать модуль random:</p>
          <pre><code class="python">>>> import random</code></pre>
          <p>Или импортировать отдельные функции из него:</p>
          <pre><code class="python">>>> from random import random, randrange, randint</code></pre>
        </div> 
