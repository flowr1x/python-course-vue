          <h2 id="title1" class="text-manual__section-title">Введение в функции</h2>
          <p>Функция в программировании представляет собой обособленный участок кода, который
              можно вызывать, обратившись к нему по имени, которым он был назван. При вызове
              происходит выполнение команд тела функции.</p>
          <p>Функции можно сравнить с небольшими программками, которые сами по себе, т. е.
              автономно, не исполняются, а встраиваются в обычную программу. Нередко их так и
              называют – подпрограммы. Других ключевых отличий функций от программ нет. Функции
              также при необходимости могут получать и возвращать данные. Только обычно они их
              получают не с ввода (клавиатуры, файла и др.), а из вызывающей программы. Сюда же они
              возвращают результат своей работы</p>
          <p>Существует множество встроенных в язык программирования функций. С некоторыми
              такими в Python мы уже сталкивались. Это print(), input(), int(), float(), str(), type(). Код их тела
              нам не виден, он где4то "спрятан внутри языка". Нам же предоставляется только интерфейс –
              имя функции.</p>
          <p>С другой стороны, программист всегда может определять свои функции. Их называют
              пользовательскими. В данном случае под "пользователем" понимают программиста, а не
              того, кто пользует программу. Разберемся, зачем нам эти функции, и как их создавать.</p>
          <p>Предположим, надо три раза подряд запрашивать на ввод пару чисел и складывать их. С этой
              целью можно использовать цикл:</p>
          <pre><code class="python">i = 0
while i &gt; 3:
    a = int(input())
    b = int(input())
    print(a+b)
    i += 1</code></pre>
          <p>Однако, что если перед каждым запросом чисел, надо выводить надпись, зачем они нужны, и
            каждый раз эта надпись разная. Мы не можем прервать цикл, а затем вернуться к тому же
            циклу обратно. Придется отказаться от него, и тогда получится длинный код, содержащий в
            разных местах одинаковые участки:</p>
          <pre><code class="python">print("Сколько бананов и ананасов для обезьян?")
a = int(input())
b = int(input())
print("Всего", a+b, "шт.")

print("Сколько жуков и червей для ежей?")
a = int(input())
b = int(input())
print("Всего", a+b, "шт.")

print("Сколько рыб и моллюсков для выдр?")
a = int(input())
b = int(input())
print("Всего", a+b, "шт.")</code></pre>
