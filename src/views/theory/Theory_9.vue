          <h2 id="title2" class="text-manual__section-title">Определение функции. Оператор def</h2>
          <div class="text-manual__section-block">
            <p>В языке программирования Python функции определяются с помощью оператора def.
              Рассмотрим код:</p>
            <pre><code class="python">def countFood():
a = int(input())
b = int(input())
print("Всего", a+b, "шт.")</code></pre>
            <p>Это пример определения функции. Как и другие сложные инструкции вроде условного
              оператора и циклов функция состоит из заголовка и тела. Заголовок оканчивается двоеточием
              и переходом на новую строку. Тело имеет отступ.</p>
            <p>Ключевое слово def сообщает интерпретатору, что перед ним определение функции. За def
              следует имя функции. Оно может быть любым, также как и всякий идентификатор, например,
              переменная. В программировании весьма желательно давать всему осмысленные имена. Так
              в данном случае функция названа "посчитатьЕду" в переводе на русский.</p>
            <p>После имени функции ставятся скобки. В приведенном примере они пустые. Это значит, что
              функция не принимает никакие данные из вызывающей ее программы. Однако она могла бы
              их принимать, и тогда в скобках были бы указаны так называемые параметры.</p>
            <p>После двоеточия следует тело, содержащее инструкции, которые выполняются при вызове
              функции. Следует различать определение функции и ее вызов. В программном коде они не
              рядом и не вместе. Можно определить функцию, но ни разу ее не вызвать. Нельзя вызвать
              функцию, которая не была определена. Определив функцию, но ни разу не вызвав ее, вы
              никогда не выполните ее тела.</p>
          </div> 
