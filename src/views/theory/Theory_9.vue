        <section class="text-manual__doc-section">
          <h2 id="title3" class="text-manual__section-title">Вызов функции</h2>
          <div class="text-manual__section-block">
            <p>Рассмотрим полную версию программы с функцией:</p>
            <pre><code class="python">def countFood():
    a = int(input())
    b = int(input())
    print("Всего", a+b, "шт.")

print("Сколько бананов и ананасов для обезьян?")
countFood()

print("Сколько жуков и червей для ежей?")
countFood()

print("Сколько рыб и моллюсков для выдр?")
countFood()</code></pre>
            <p>После вывода на экран каждого информационного сообщения осуществляется вызов
                функции, который выглядит просто как упоминание ее имени со скобками. Поскольку в
                функцию мы ничего не передаем скобки опять же пустые. В приведенном коде функция
                вызывается три раза.</p>
            <p>Когда функция вызывается, поток выполнения программы переходит к ее определению и
                начинает исполнять ее тело. После того, как тело функции исполнено, поток выполнения
                возвращается в основной код в то место, где функция вызывалась. Далее исполняется
                следующее за вызовом выражение.</p>
            <p>В языке Python определение функции должно предшествовать ее вызовам. Это связано с тем,
                что интерпретатор читает код строка за строкой и о том, что находится ниже по течению, ему
                еще неизвестно. Поэтому если вызов функции преддшествует ее определению, то возникает
                ошибка (выбрасывается исключение NameError):</p>
            <pre><code class="python">print("Сколько бананов и ананасов для обезьян?")
countFood()

print("Сколько жуков и червей для ежей?")
countFood()

print("Сколько рыб и моллюсков для выдр?")
countFood()

def countFood():
    a = int(input())
    b = int(input())
    print("Всего", a+b, "шт.")</code></pre>
              <p>Результат:</p>
              <pre><code class="python">Сколько бананов и ананасов для обезьян?
Traceback (most recent call last):
 File "test.py", line 2, in &lt;module&gt;
 countFood()
NameError: name 'countFood' is not defined</code></pre>
              <p>Для многих компилируемых языков это не обязательное условие. Там можно определять и
                вызывать функцию в произвольных местах программы. Однако для удобочитаемости кода
                программисты даже в этом случае предпочитают соблюдать определенные правила.</p>
          </div>
