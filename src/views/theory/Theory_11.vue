          <p>Если интерпретатор Питона, выполняя тело функции, встречает return, то он "забирает"
              значение, указанное после этой команды, и "уходит" из функции. </p>
          <pre><code class="python">def cylinder():
    r = float(input())
    h = float(input())
    # площадь боковой поверхности цилиндра:
    side = 2 * 3.14 * r * h
    # площадь одного основания цилиндра:
    circle = 3.14 * r**2
    full = side + 2 * circle
    # полная площадь цилиндра:
    return full
    
square = cylinder()
print(square)</code></pre>
          <p>Пример выполнения:</p>
          <pre><code class="pyton">3
7
188.4</code></pre>
