        <div class="text-manual__section-block">
          <p>Функции могут передавать какие-либо данные из своих тел в основную ветку программы.
              Говорят, что функция возвращает значение. В большинстве языков программирования, в том
              числе Python, выход из функции и передача данных в то место, откуда она была вызвана,
