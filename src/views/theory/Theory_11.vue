            <h2 id="title1" class="text-manual__section-title">Возврат несколько значений</h2>
            <div class="text-manual__section-block">
              <p>В Питоне позволительно возвращать из функции несколько объектов, перечислив их через
                  запятую после команды return:</p>
              <pre><code class="python">def cylinder():
    r = float(input())
    h = float(input())
    side = 2 * 3.14 * r * h
    circle = 3.14 * r**2
    full = side + 2 * circle
    return side, full

sCyl, fCyl = cylinder()
print("Площадь боковой поверхности %.2f" % sCyl)
print("Полная площадь %.2f" % fCyl)</code></pre>
              <p>Из функции cylinder() возвращаются два значения. Первое из них присваивается переменной
                  sCyl, второе – fCyl. Возможность такого группового присвоения – особенность Python,
                  обычно не характерная для других языков:</p>
              <pre><code class="python">>>> a, b, c = 10, 15, 19
>>> a
10
>>> b
15
>>> c
19</code></pre>
              <p>Фокус здесь в том, что перечисление значений через запятую (например, 10, 15, 19) создает
                  объект типа tuple. На русский переводится как "кортеж". Это разновидность структур
                  данных, которые будут изучены позже.</p>
              <p>Когда же кортеж присваивается сразу нескольким переменным, то происходит сопоставление
                  его элементов соответствующим в очереди переменным. Это называется распаковкой.</p>
              <p>Таким образом, когда из функции возвращается несколько значений, на самом деле из нее
                  возвращается один объект класса tuple. Перед возвратом эти несколько значений
                  упаковываются в кортеж. Если же после оператора return стоит только одна переменная или
                  объект, то ее/его тип сохраняется как есть.</p>
              <p>Распаковка не является обязательной. Будет работать и так:</p>
              <pre><code class="python">…
print(cylinder())</code></pre>
              <p>Пример выполнения:</p>
              <pre><code class="python">4
3
(75.36, 175.84)</code></pre>
              <p>На экран выводится кортеж, о чем говорят круглые скобки. Его также можно присвоить
                одной переменной, а потом вывести ее значение на экран. </p>
